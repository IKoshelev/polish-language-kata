export function prepareCards(): NumeralDict[] {
  return numeralsDictBase.map((x) => {
    const [numeral, ...forms] = x;

    const row: NumeralDict = {
      numeral,
      cards: [
        "Ile?",
        "Który?",
        "Która?",
        "Które?",
        "Którego?",
        "Której?",
        "O którym?",
        "O której?",
      ].map((question, index) => ({
        textWhenClosed: `${question} ${numeral}`,
        textWhenOpened: forms[index],
        id: Math.random(),
      })) as any,
    };

    return row;
  });
}
